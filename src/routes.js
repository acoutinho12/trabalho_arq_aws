const express = require("express");
const TimeController = require("./controllers/TimeController");
const JogadorController = require("./controllers/JogadorController");
const TransferenciaController = require("./controllers/TransferenciaController");

const routes = express.Router();

////////////////////////////////////////////////////////////////TIMES////////////////////////////////////////////////////////////////
routes.post("/times", TimeController.create);
////////////////////////////////////////////////////////////////TIMES////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////JOGADORES////////////////////////////////////////////////////////////////
routes.get("/jogadores/:id", JogadorController.read);
routes.post("/jogadores", JogadorController.create);
routes.put("/jogadores/:id", JogadorController.update);
routes.delete("/jogadores/:id", JogadorController.delete);
////////////////////////////////////////////////////////////////JOGADORES////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////TRANSFERENCIAS////////////////////////////////////////////////////////////////
routes.post("/transferencias", TransferenciaController.create);
////////////////////////////////////////////////////////////////TRANSFERENCIAS////////////////////////////////////////////////////////////////

module.exports = routes;
